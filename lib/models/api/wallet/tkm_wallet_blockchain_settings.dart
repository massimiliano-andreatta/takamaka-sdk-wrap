class Charset {
  String? _name;
  List<String>? _aliases;

  Charset({String? name, List<String>? aliases}) {
    if (name != null) {
      this._name = name;
    }
    if (aliases != null) {
      this._aliases = aliases;
    }
  }

  String? get name => _name;
  set name(String? name) => _name = name;
  List<String>? get aliases => _aliases;
  set aliases(List<String>? aliases) => _aliases = aliases;

  Charset.fromJson(Map<String, dynamic> json) {
    _name = json['name'];
    _aliases = json['aliases'].cast<String>();
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = new Map<String, dynamic>();
    data['name'] = this._name;
    data['aliases'] = this._aliases;
    return data;
  }
}
